#include <unistd.h>

int	main(void)
{
	char	c;

	c = 58;
	while (--c >= 48)
		write(1, &c, 1);
	write(1, "\n", 1);
}
// 58 == ':' in table ascii and --c ==> 58 - 1 == 57 == '9'
