#include<unistd.h>

int	main(void)
{
	int		i;
	char	a;
	char	b;

	i = 0;
	a = 'a';
	while (i <= 25)
	{
		if (i % 2 == 0)
			write(1, &a, 1);
		else
		{
			b = a - 32;
			write(1, &b, 1);
		}
		a++;
		i++;
	}
	write(1, "\n", 1);
}
