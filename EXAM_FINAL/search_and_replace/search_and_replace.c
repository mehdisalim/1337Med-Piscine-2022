#include<unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	if (ac == 4)
	{
		while (*av[1])
		{
			if (av[2][1] || av[3][1])
				break ;
			if (*av[1] == av[2][0])
				write(1, &av[3][0], 1);
			else
				write(1, &*av[1], 1);
			av[1]++;
		}
	}
	write(1, "\n", 1);
}
